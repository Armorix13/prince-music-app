import mongoose from "mongoose";

const feedbackSchema = new mongoose.Schema(
  {
    musicianId: {
      type: Number,
      required: true,
      index: true
    },
    title: {
      type: String,
      required: true,
      trim: true
    },
    description: {
      type: String,
      required: true,
      trim: true
    },
    firstName: {
      type: String,
      required: true,
      trim: true
    },
    lastName: {
      type: String,
      required: true,
      trim: true
    },
    email: {
      type: String,
      required: true,
      lowercase: true,
      trim: true
    }
  },
  {
    timestamps: true
  }
);

export const Feedback = mongoose.model("Feedback", feedbackSchema);


